RESIZE_ROWS=64
RESIZE_COLS=64

def crop_resize_image(img):
   rows,cols,ch = img.shape
   HORIZON = 60
   BONNET = 136
   # # note: numpy arrays are (row, col)!
   img_crop = img[HORIZON:BONNET, 0:cols, :]
   img_resize = cv2.resize(img_crop,(RESIZE_COLS,RESIZE_ROWS), interpolation=cv2.INTER_AREA)
   #image = image/255.-.5
   return img_resize


sio = socketio.Server()
